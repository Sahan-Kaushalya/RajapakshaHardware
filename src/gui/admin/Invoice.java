    private void GenerateID() {

        try {
            ResultSet resultset = MySQL.executeSearch("SELECT MAX(`id`) AS max_id FROM `invoice`");

            if (resultset.next()) {
                String maxID = resultset.getString("max_id");
                if (maxID != null) {
                    int ID = Integer.parseInt(maxID.replace("INV_", ""));
                    iID = String.format("INV_%03d", ID + 1);
                    jTextField1.setText(iID);
                } else {
                    iID = "INV_001";
                    jTextField1.setText(iID);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    String name = null;

    private void loadEmployee() {

        try {

            String email = String.valueOf(jComboBox2.getSelectedItem());

            String query = "SELECT * FROM `users`";

            if (!email.equals("null") && !email.equals("Select")) {
                query += " WHERE `email`='" + email + "'";
            }
            ResultSet resultSet = MySQL.executeSearch(query);
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            while (resultSet.next()) {
                vector.add(resultSet.getString("email"));
                name = resultSet.getString("fname") + " " + resultSet.getString("lname");
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadPaymentMethod() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `payment_method`");

            Vector<String> vector = new Vector<>();

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                paymentMethodMap.put(resultSet.getString("name"), resultSet.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadInvoiceItems() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        double total = 0;

        for (InvoiceItems invoiceItem : invoiceItemMap.values()) {
            Vector<String> vector = new Vector<>();
            vector.add(invoiceItem.getProductId());
            vector.add(invoiceItem.getProductName());
            vector.add(String.valueOf(invoiceItem.getQty()));
            vector.add(String.valueOf(invoiceItem.getPrice()));

            double itemTotal = invoiceItem.getQty() * invoiceItem.getPrice();
            vector.add(String.valueOf(itemTotal));

            vector.add(invoiceItem.getStockId());
            total += itemTotal;

            model.addRow(vector);
        }

        jFormattedTextField1.setText(String.valueOf(total));
    }

