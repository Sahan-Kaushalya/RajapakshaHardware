    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String quantity = jTextField7.getText();

        if (productId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a product to add", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (quantity.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter quantity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!quantity.matches("^(0|[1-9]\\d*)?(\\.\\d+)?(?<=\\d)$") || Double.parseDouble(quantity) == 0) {
            JOptionPane.showMessageDialog(this, "Please enter valid quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            jFormattedTextField3.setText("");
        } else if (Double.parseDouble(quantity) > Double.parseDouble(qty)) {
            JOptionPane.showMessageDialog(this, "Your entered quantity exceed the avaliable quantity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            InvoiceItems invoiceItem = new InvoiceItems();
            invoiceItem.setStockId(stockId);
            invoiceItem.setProductId(productId);
            invoiceItem.setProductName(productName);
            invoiceItem.setQty(Double.parseDouble(quantity));
            invoiceItem.setPrice(Double.parseDouble(productPrice));

            if (invoiceItemMap.get(stockId) == null) {
                invoiceItemMap.put(stockId, invoiceItem);
                clear();
                loadInvoiceItems();
            } else {

                InvoiceItems found = invoiceItemMap.get(stockId);

                double newQty = found.getQty() + Double.parseDouble(quantity);

                if (newQty > Double.parseDouble(qty)) {
                    JOptionPane.showMessageDialog(this, "Your quantity exceed available quantity", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    found.setQty(newQty);
                    clear();
                    loadInvoiceItems();
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

